    public function filterLikeCampus(Campus $campus): ?array
    {
        $qb = $this -> createQueryBuilder('c');
        $qb -> andWhere('c.name LIKE :campusName')
            -> setParameter(':campusName', '%' . $campus -> getName() . '%');

        $query = $qb -> getQuery();
        return $query -> getResult();
    }

